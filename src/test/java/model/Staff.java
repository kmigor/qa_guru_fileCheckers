package model;

public class Staff {

    private Integer id;
    private String firstName;
    private String lastName;
    private String vacation;

    public Integer getId() {
        return id;
    }

    public String getFirstName() {
        return firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public String getVacation() {
        return vacation;
    }
}